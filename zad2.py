# Петя и Катя – брат и сестра. Петя – студент, а Катя – школьница. Петя помогает Кате по математике. Он задумывает два натуральных 
# числа X и Y (X,Y≤1000), а Катя должна их отгадать. Для этого Петя делает две подсказки. Он называет сумму этих чисел S и их 
# произведение P. Помогите Кате отгадать задуманные Петей числа.

def find_numbers(S, P):
    for X in range(1, 1001):
        Y = S - X  
        if Y < 1:
            break  
        if X * Y == P:
            return X, Y 
    return None, None  

S = int(input("Введите сумму S: "))
P = int(input("Введите произведение P: "))

X, Y = find_numbers(S, P)

if X is not None:
    print(f"Задуманные числа: X = {X}, Y = {Y}")
else:
    print("Не удалось найти подходящие числа для заданных S и P.")
